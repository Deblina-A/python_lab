import math

sin_60 = math.sin(math.radians(60))

cos_pi = math.cos(math.pi)

sin_value = math.sin(0.8660254037844386)

tan_90 = math.tan(math.radians(90))

print(f"sin(60 degrees) is {sin_60:.2f}")
print(f"cos(pi) is {cos_pi:.2f}")
print(f"sin(0.8660254037844386) is {sin_value:.2f}")
print(f"tan(90 degrees) is {tan_90:.2f}")